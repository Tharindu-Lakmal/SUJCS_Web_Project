    <h5 style="font-size: 14px;">Language</h5>
    <p style="color: grey;">The language of the manuscript must be in English (British standard).</p>
    <h5 style="font-size: 14px;" ,style="color: grey;">Length of Paper</h5>
    <p style="color: grey;">The paper's length must fall within the range of 7000 to 10000 words, encompassing the
      references and annexures. Any submission<br>
      exceeding 10000 words will be declined. Articles should be typed in 12-point Times New Roman font, with 1.5 line
      spacing, on one side of<br>
      A4 paper, maintaining normal margins (Top 1”, Bottom 1”, Left 1”, and Right 1”). Authors are encouraged to express
      ideas concisely<br>
      while ensuring clarity in their writing.</p>
    <h5 style="font-size: 14px;">Title Page</h5>
    <p style="color: grey;">The Title page is a separate page before the text. It should include the following
      information:</p>
    <div class="subtopics">
      <p style="color:gray;" ,style="font-size:16;"><b>Title</b></p>
      <p style="color:gray;">The title should be concise and informative. Abbreviations and formulae should be avoided
        where possible.</p>
      <p style="color:gray;" ,style="font-size:16;"><b>Author’s Names & Affiliations</b></p>
      <p style="color:gray;">Please provide the author's initials followed by their last name. Include the authors'
        affiliation addresses (the location<br>
        where the research was conducted) beneath their names. Use lowercase superscript letters to denote each
        affiliation,<br>
        corresponding to the appropriate address. Include the complete postal address for each affiliation, including
        the country,<br>
        and provide the email address for each author.</p>
      <p style="color:gray;" ,style="font-size:16;"><b>Corresponding Author</b></p>
      <p style="color:gray;">Specify individuals who are ready to manage communication throughout the entire peer review
        process, publication,<br>
        and post-publication. Include their email addresses, complete postal addresses, and telephone numbers with<br>
        country and area codes.</p>
    </div>

    <p></p> <!-- Empty paragraph for space -->


    <h5 style="font-size: 14px;">Abstract</h5>
    <p style="color: grey;">Compose a brief and professional summary of the research within a 250-word limit. Clearly
      state the research purpose, highlight
      principal findings, and summarize key conclusions. Avoid including references in this standalone abstract.</p>

    <h5 style="font-size: 14px;">Key Words</h5>
    <p style="color: grey;">Immediately after the abstract, a maximum of 5 keywords, avoiding general and plural terms
      and multiple concepts
      (Ex:'and', 'of')<br>should be provided. Keywords should be presented. The keywords should be arranged in
      alphabetical order.</p>
    <h5 style="font-size: 14px;">Literature Review</h5>
    <p style="color: grey;">This section should provide a sufficient evaluation of the theoretical/empirical literature
      to identify the knowledge gaps.</p>
    <h5 style="font-size: 14px;">Materials and Methods</h5>
    <p style="color: grey;">This section should contain adequate and detailed information of all the procedures and
      steps followed.</p>
    <h5 style="font-size: 14px;">Results & Discussion</h5>
    <p style="color: grey;">This section should describe the results and the outputs of the research work.</p>
    <h5 style="font-size: 14px;">Conclusions</h5>
    <p style="color: grey;">This section should clearly explain the important conclusions of the work highlighting its
      significance, relevance and values.</p>
    <h5 style="font-size: 14px;">Sub-division of the Article</h5>
    <p style="color: grey;">This section should clearly explain the important conclusions of the work highlighting its
      significance, relevance and values.</p>
    <h5 style="font-size: 14px;">Tables and Figures</h5>
    <p style="color: grey;">This section should clearly explain the important conclusions of the work highlighting its
      significance, relevance and values.</p>
    <div class="tables_figures_subtopics">
      <p style="color:gray;" ,style="font-size:16;"><b>Table</b></p>
      <p style="color:gray;">Number the tables sequentially using Arabic numerals, following the order of their
        appearance in the text. Present each table<br>
        on an individual page with double-spacing, accompanied by a brief descriptive title placed directly above it.
        Include necessary<br>
        footnotes below the tables. Authors are required to submit intricate tables in a print-ready format."</p>
      <p style="color:gray;" ,style="font-size:16;"><b>Figures</b></p>
      <p style="color:gray;">Prepare figures in a polished state suitable for publication. Assign Arabic numerals to
        each figure and note the top and authors<br>
        on the back of each. Lettering on illustrations should be of professional quality or generated through
        high-resolution computer<br>
        graphics, ensuring it is large enough for suitable reduction in publication. Avoid embedding figures within the
        text. Acceptable<br>
        digital artwork should have a minimum resolution of 300 dpi and be in eps or TIFF formats. For comprehensive
        guidelines on<br>
        creating electronic artwork, please refer to our website at
        https://www.elsevier.com/artwork-and-media-instructions.</p>
    </div>

    <h5 style="font-size: 14px;">Acknowledgement</h5>
    <p style="color: grey;">If the research is sponsored or supported by an organization, please indicate it.</p>
    <h5 style="font-size: 14px;">Reference List</h5>
    <div class="reference_list_subtopics">
      <p style="color:gray;" ,style="font-size:16;"><b>Citation in Text</b></p>
      <p style="color:gray;">Match citations in the text with entries in the reference list. Provide full references for
        abstract citations.<br>
        Avoid including unpublished. results or personal communications in the reference list, but if necessary, use
        standard referencing with 'Unpublished Results' or '<br>
        Personal Communication.' Note that 'in press' means the item has been accepted for publication.</p>
      <p style="color:gray;" ,style="font-size:16;"><b>References</b></p>
      <p style="color:gray;">When citing online sources, provide the complete URL and mention the date you last accessed
        the information. If available, include<br>
        additional details such as DOI, author names, publication dates, or source references. You can choose to list
        web references separately<br>
        or integrate them into the main reference list.</p>
      <p style="color:gray;" ,style="font-size:16;"><b>Data Reference</b></p>
      <p style="color:gray;">This journal encourages authors to mention and reference relevant datasets in their
        manuscripts. When citing datasets, include author<br>
        names, dataset title, data repository, version (if applicable), year, and a global persistent identifier in the
        Reference List. Insert [dataset]<br>
        before the reference to indicate it as a data reference; however, this identifier won't appear in the published
        article.</p>
